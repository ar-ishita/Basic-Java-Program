package com.work.is;

public class SwapNumberProgram {

    public static void main(String[] args){
        int a = 10;
        int b =30;
        int temp = 0;

        temp = a;
        a = b;
        b = temp;

        System.out.println("Value of a  : " +a);
        System.out.println("Value of b : " +b);
    }
}
